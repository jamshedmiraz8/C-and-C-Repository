// how to get user input using scanf
#include <stdio.h>
int main()
{
    int num1;
    int num2;
    printf("Enter You Fast Number : ");
    scanf("%d",&num1);
    printf("Enter Your Last Number : ");
    scanf("%d",&num2);
    printf("This is Your Fast : %d \n",num1);
    printf("This is your Last : %d",num2);
}
